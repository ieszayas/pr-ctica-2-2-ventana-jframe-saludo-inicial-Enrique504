    private void botonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonActionPerformed
        // TODO add your handling code here:
        String nom;
        String apellido;
        nom=nombre.getText();
        apellido=apellidos.getText();
        
        if(nom.trim().isEmpty()){
            JOptionPane.showMessageDialog(this,"El campo de nombre esta vacio","Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
        if(apellido.trim().isEmpty()){
            JOptionPane.showMessageDialog(this,"El campo de apellidos esta vacio","Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if(nom.length() < 5){
            JOptionPane.showMessageDialog(this,"Tiene que tener el nombre longitud minimo de 5 caracteres","Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
        if(!nom.matches("[^\\d]+")){
            JOptionPane.showMessageDialog(this,"Tiene numero en el nombre","Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
        if(!apellido.matches("[^\\d]+")){
            JOptionPane.showMessageDialog(this,"Tiene numero en el apellido","Error",JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        
        
        JOptionPane.showMessageDialog(this,"Â¡Hola "+nom+" "+apellido+" !","Saluda",JOptionPane.INFORMATION_MESSAGE);
        nombre.setText("");
        apellidos.setText("");
        
    }//GEN-LAST:event_botonActionPerformed

